## -----------------------------------------------------------------------
## -----------------------------------------------------------------------
function stream_set()
{
    declare -n ind="$1"; shift
    declare -a -g __todo__=("${ind[@]}")
    declare -p __todo__
    return
}

## -----------------------------------------------------------------------
## -----------------------------------------------------------------------
function stream_get()
{
    declare -n ind="$1"; shift
    declare -a -g __todo__

    ind=("${__todo__[@]}__}")
    declare -p ind
    return
}

## -----------------------------------------------------------------------
## -----------------------------------------------------------------------
function setup_pom()
{
    local path="$1"; shift

    declare -a src=()
    src+=("${path}/sadis")
    src+=("${path}/olt")
    src+=("${path}/aaa")
    stream_set 'src'

    return
}

