function show_annotations()
{
    local _tag="$1"; shift

    banner "$LINENO" "show_annotations (LINENO:$LINENO) (tag:$_tag)"
    
    set -x
    git for-each-ref refs/tags/"${_tag}" --format='%(contents)'
    set +x

    return
}
